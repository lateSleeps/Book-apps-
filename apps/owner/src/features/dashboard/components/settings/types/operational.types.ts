// ── DayOfWeek ─────────────────────────────────────────────────────────────────

/**
 * Day of week index.
 *
 * Uses JavaScript Date.prototype.getDay() convention — NOT ISO 8601.
 *
 *   0 = Sunday    (Minggu)
 *   1 = Monday    (Senin)
 *   2 = Tuesday   (Selasa)
 *   3 = Wednesday (Rabu)
 *   4 = Thursday  (Kamis)
 *   5 = Friday    (Jumat)
 *   6 = Saturday  (Sabtu)
 *
 * This matches:
 *   - JavaScript Date.getDay()
 *   - date-fns getDay()
 *   - Supabase business_hours.day_of_week (integer 0-6)
 *   - customer/lib/date-utils.ts buildCalendarGrid()
 *   - customer/lib/validation.ts validateDateNotClosed()
 */
export type DayOfWeek = 0 | 1 | 2 | 3 | 4 | 5 | 6;

